package engine;

/**
 * Timer.
 * @author anhcx
 */
public class Timer extends com.golden.gamedev.object.Timer {
    
    /**
     * Constructor.
     * @param i period of time.
     */
    public Timer(int i) {
        super(i);
    }
    
    /**
     * Action to do every period of time.
     * @param l period of time.
     * @return boolean
     */
    @Override
    public boolean action(long l) {
        return super.action(l);
    }
    
}
